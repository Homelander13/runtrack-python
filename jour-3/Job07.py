
def determiner_type_developpeur(langage):
    if langage == "JavaScript":
        print("Tu es un développeur web")
    elif langage == "python":
        print("Tu es un développeur IA")
    elif langage == "java":
        print("Tu es un développeur logiciel")
    elif langage == "reactjs":
        print("Tu es un développeur mobile")
    else:
        print("Un jour, je serai le meilleur développeur...")


determiner_type_developpeur("JavaScript")
determiner_type_developpeur("python")
determiner_type_developpeur("java")
determiner_type_developpeur("reactjs")
determiner_type_developpeur("C++")  
